// 工具函數
const utils = {
    // 格式化數字
    formatNumber: (num) => {
        return num.toLocaleString();
    },
    
    // 取得排名勳章
    getRankMedal: (rank) => {
        switch(rank) {
            case 1: return '🥇';
            case 2: return '🥈';
            case 3: return '🥉';
            default: return rank;
        }
    },
    
    // 取得組別徽章
    getGroupBadge: (group) => {
        const badges = {
            'A': '<span class="group-badge badge-a">⭐新星</span>',
            'B': '<span class="group-badge badge-b">🚀飛耀</span>',
            'C': '<span class="group-badge badge-c">👑菁英</span>'
        };
        return badges[group] || '';
    },
    
    // 取得區域徽章
    getRegionBadge: (region) => {
        return `<span class="region-badge region-${region}">${region}</span>`;
    },
    
    // 篩選資料
    filterByGroup: (group) => {
        return competitionData.filter(store => store.group === group);
    },
    
    // 按金額排名排序（組內）
    sortByAmountRank: (data) => {
        return data.sort((a, b) => a.金額排名 - b.金額排名);
    },
    
    // 按客戶數排名排序（組內）
    sortByCustomerRank: (data) => {
        return data.sort((a, b) => a.客戶數排名 - b.客戶數排名);
    },
    
    // 按客戶積分排序
    sortByCustomerScore: (data) => {
        return data.sort((a, b) => b.customerScore - a.customerScore);
    },
    
    // 按業績積分排序
    sortByAmountScore: (data) => {
        return data.sort((a, b) => b.amountScore - a.amountScore);
    }
};

